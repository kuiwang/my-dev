package com.scott.dev.taobao.api.hoteldaogou.domain;

/**
 * 名值对
 */
public class NameValuePair {

    /**
     * 名值对的编码
     */
    private String code;

    /**
     * 名值对的文本描述
     */
    private String name;

    public String getCode() {
        return this.code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
